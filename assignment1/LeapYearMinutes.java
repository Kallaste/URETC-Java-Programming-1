//Calculates the number of minutes in a leap year
//Uses the fact that there are 525,600 minutes in a 365 day year
public class LeapYearMinutes
{
    public static void main(String[] args)
    {
        System.out.print("The number of minutes in a leap year is ");
        System.out.println( 60*24+525600); // 60 min/hr times 24 hr/day  525600 min
    }
}
